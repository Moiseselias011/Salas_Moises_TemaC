
package salas_moises_temac;


public class Interseccion {
    
    private String calleA;
    private String calleB;
    private int altura;
    private String barrio;

    public Interseccion() {
    }

    public Interseccion(String calleA, String calleB, int altura, String barrio) {
        this.calleA = calleA;
        this.calleB = calleB;
        this.altura = altura;
        this.barrio = barrio;
    }

}

